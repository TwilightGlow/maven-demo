package com.example.algorithm.tree;

import lombok.Data;
import lombok.Getter;

/**
 * @author zhangjw54
 */
@Data
public class TreeNode {

    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
